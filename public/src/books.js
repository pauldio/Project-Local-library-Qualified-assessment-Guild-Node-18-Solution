//Create a function findAuthorById to find two parameters author by ID
function findAuthorById(authors, id) {
  let fdAuthorId = authors.find((author) => author.id === id);
  return fdAuthorId;
}

//Create a function findBookById to find two parameters books and ID
function findBookById(books, id) {
  let foundBookId = books.find((books) => books.id === id);
  return foundBookId;
}

